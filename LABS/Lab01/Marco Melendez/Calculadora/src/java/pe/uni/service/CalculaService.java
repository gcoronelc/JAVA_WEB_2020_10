package pe.uni.service;

public class CalculaService {

    public double calcSuma(double n1, double n2) {
        return (n1 + n2);
    }

    public double calcResta(double n1, double n2) {
        return (n1 - n2);
    }

    public double calcMultiplicacion(double n1, double n2) {
        return (n1 * n2);
    }

    public double calcDivision(double n1, double n2) {
        return (n1 / n2);
    }

    public double calcResto(double n1, double n2) {
        return (n1 % n2);
    }

}
